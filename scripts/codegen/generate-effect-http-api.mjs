import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const root = new URL(".", import.meta.url);
const repoRoot = new URL("../../", root);
const inputPath = codegenPath("CODEGEN_OPENAPI_PATH", "apps/api/openapi/openapi.json");
const outputPath = codegenPath("CODEGEN_HTTP_API_OUTPUT_PATH", "packages/sdk-core/src/generated/http-api.ts");

const document = JSON.parse(await readFile(inputPath, "utf8"));
const operations = collectOperations(document);
const groups = groupOperations(operations);

const lines = ["// Generated by scripts/codegen/generate-effect-http-api.mjs", "// Do not edit by hand.", 'import { HttpApi, HttpApiEndpoint, HttpApiGroup, HttpApiSchema } from "effect/unstable/httpapi"', 'import * as S from "./schemas"', ""];

for (const group of groups) {
  lines.push(`const ${group.variableName} = HttpApiGroup.make(${JSON.stringify(group.name)})`);
  for (const operation of group.operations) {
    lines.push(`  .add(${renderEndpoint(operation)})`);
  }
  lines.push("");
}

lines.push('export class ChalkApi extends HttpApi.make("ChalkApi")');
for (const group of groups) {
  lines.push(`  .add(${group.variableName})`);
}
lines.push("{}");

await mkdir(path.dirname(fileURLToPath(outputPath)), { recursive: true });
await writeFile(outputPath, `${lines.join("\n")}\n`);

function codegenPath(envName, fallback) {
  const value = process.env[envName];
  if (value) {
    return pathToFileURL(path.resolve(value));
  }

  return new URL(fallback, repoRoot);
}

function collectOperations(document) {
  const operations = [];
  for (const [routePath, pathItem] of Object.entries(document.paths ?? {})) {
    for (const [method, operation] of Object.entries(pathItem ?? {})) {
      if (!operation || typeof operation !== "object" || !operation.operationId) {
        continue;
      }

      const operationName = pascalCase(operation.operationId);
      operations.push({
        method,
        routePath,
        effectPath: routePath.replaceAll("{", ":").replaceAll("}", ""),
        operationId: operation.operationId,
        operationName,
        groupName: operationGroupName(operation.operationId),
        success: successResponse(operation.responses),
        errors: errorResponses(operation.responses),
        hasPathParams: hasParameters(operation.parameters, "path"),
        hasQueryParams: hasParameters(operation.parameters, "query"),
        hasRequestBody: Boolean(operation.requestBody?.content?.["application/json"]?.schema),
      });
    }
  }

  return operations.sort((left, right) => left.operationId.localeCompare(right.operationId));
}

function groupOperations(operations) {
  const groupsByName = new Map();
  for (const operation of operations) {
    if (!groupsByName.has(operation.groupName)) {
      groupsByName.set(operation.groupName, []);
    }
    groupsByName.get(operation.groupName).push(operation);
  }

  return [...groupsByName.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([name, operations]) => ({
      name,
      variableName: `${camelCase(name)}Group`,
      operations,
    }));
}

function renderEndpoint(operation) {
  const options = [];

  if (operation.hasPathParams) {
    options.push(`  params: S.${operation.operationName}PathParamsSchema`);
  }
  if (operation.hasQueryParams) {
    options.push(`  query: S.${operation.operationName}QueryParamsSchema`);
  }
  if (operation.hasRequestBody) {
    options.push(`  payload: S.${operation.operationName}RequestBodySchema`);
  }
  if (operation.success) {
    const schema = operation.success.hasSchema ? `S.${operation.operationName}ResponseSchema` : `HttpApiSchema.Empty(${operation.success.status})`;
    options.push(`  success: ${schema}.pipe(HttpApiSchema.status(${operation.success.status}))`);
  }
  if (operation.errors.length > 0) {
    options.push(`  error: [${operation.errors.map((error) => `S.${error.className}Schema.pipe(HttpApiSchema.status(${error.status}))`).join(", ")}]`);
  }

  if (options.length === 0) {
    return `HttpApiEndpoint.${methodFunction(operation.method)}(${JSON.stringify(operation.operationId)}, ${JSON.stringify(operation.effectPath)})`;
  }

  return `HttpApiEndpoint.${methodFunction(operation.method)}(${JSON.stringify(operation.operationId)}, ${JSON.stringify(operation.effectPath)}, {\n${options.join(",\n")}\n})`;
}

function methodFunction(method) {
  switch (method.toLowerCase()) {
    case "delete":
      return "delete";
    case "get":
    case "patch":
    case "post":
    case "put":
      return method.toLowerCase();
    default:
      throw new Error(`Unsupported method: ${method}`);
  }
}

function successResponse(responses = {}) {
  const status =
    Object.keys(responses)
      .sort()
      .find((value) => /^2\d\d$/.test(value)) ??
    Object.keys(responses)
      .sort()
      .find((value) => /^3\d\d$/.test(value));

  if (!status) {
    return undefined;
  }

  return {
    status: Number(status),
    hasSchema: Boolean(responses[status]?.content?.["application/json"]?.schema),
  };
}

function errorResponses(responses = {}) {
  const errors = [];
  const seen = new Set();
  for (const [status, response] of Object.entries(responses)) {
    for (const code of response["x-chalk-error-codes"] ?? []) {
      const key = `${status}:${code}`;
      if (seen.has(key)) {
        continue;
      }
      seen.add(key);
      errors.push({
        status: Number(status),
        className: errorClassName(code),
      });
    }
  }

  return errors.sort((left, right) => left.status - right.status || left.className.localeCompare(right.className));
}

function hasParameters(parameters = [], location) {
  return parameters.some((parameter) => parameter.in === location);
}

function operationGroupName(operationId) {
  if (/google|login|logout|register/i.test(operationId)) {
    return "auth";
  }
  if (operationId === "getMe") {
    return "me";
  }
  if (/region/i.test(operationId)) {
    return "regions";
  }
  if (/auditLog/i.test(operationId)) {
    return "auditLogs";
  }
  if (/membership/i.test(operationId)) {
    return "memberships";
  }
  if (/roomSession/i.test(operationId)) {
    return "roomSessions";
  }
  if (/room/i.test(operationId)) {
    return "rooms";
  }
  if (/recording/i.test(operationId)) {
    return "recordings";
  }
  if (/transcript/i.test(operationId)) {
    return "transcripts";
  }
  if (/tenant/i.test(operationId)) {
    return "tenants";
  }
  if (/user/i.test(operationId)) {
    return "users";
  }
  return "default";
}

function errorClassName(code) {
  const name =
    {
      internal_error: "InternalError",
    }[code] ?? pascalCase(code);

  return name.endsWith("Error") ? name : `${name}Error`;
}

function pascalCase(value) {
  return value
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part) => `${part.slice(0, 1).toUpperCase()}${part.slice(1)}`)
    .join("");
}

function camelCase(value) {
  const pascal = pascalCase(value);
  return `${pascal.slice(0, 1).toLowerCase()}${pascal.slice(1)}`;
}
