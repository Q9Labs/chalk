import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const root = new URL(".", import.meta.url);
const repoRoot = new URL("../../", root);
const inputPath = codegenPath("CODEGEN_OPENAPI_PATH", "apps/api/openapi/openapi.json");
const outputPath = codegenPath("CODEGEN_EFFECT_OUTPUT_PATH", "packages/sdk-core/src/generated/schemas.ts");

const document = JSON.parse(await readFile(inputPath, "utf8"));
const schemas = document.components?.schemas ?? {};
const operationSchemas = collectOperationSchemas(document);
const operationErrors = collectOperationErrors(document);
const errorDefinitions = collectErrorDefinitions(operationErrors);
const headerDefinitions = collectHeaderDefinitions(operationSchemas);

const lines = ["// Generated by scripts/codegen/generate-effect-schemas.mjs", "// Do not edit by hand.", 'import { Schema, SchemaGetter } from "effect"', ""];

for (const [name, schema] of orderedSchemas(schemas)) {
  lines.push(`export const ${name}Schema = ${renderSchema(schema, { schemaName: name })}`);
  lines.push(`export type ${name} = typeof ${name}Schema.Type`);
  lines.push("");
}

for (const header of headerDefinitions) {
  lines.push(`export const ${header.schemaName} = ${header.rendered}`);
  lines.push(`export type ${header.typeName} = typeof ${header.schemaName}.Type`);
  lines.push("");
}

for (const operation of operationSchemas) {
  for (const schema of operation.schemas) {
    lines.push(`export const ${schema.name}Schema = ${renderSchema(schema.schema)}`);
    lines.push(`export type ${schema.name} = typeof ${schema.name}Schema.Type`);
    lines.push("");
  }
}

for (const error of errorDefinitions) {
  lines.push(`export class ${error.className} extends Schema.TaggedErrorClass<${error.className}>()(${JSON.stringify(error.className)}, {`);
  lines.push("  error: Schema.Struct({");
  lines.push(`    code: Schema.Literal(${JSON.stringify(error.code)}),`);
  lines.push("    message: Schema.String");
  lines.push("  })");
  lines.push("}) {}");
  lines.push(`export const ${error.className}WireSchema = Schema.Struct({`);
  lines.push("  error: Schema.Struct({");
  lines.push(`    code: Schema.Literal(${JSON.stringify(error.code)}),`);
  lines.push("    message: Schema.String");
  lines.push("  })");
  lines.push("})");
  lines.push(`export const ${error.className}Schema = ${error.className}WireSchema.pipe(`);
  lines.push(`  Schema.decodeTo(${error.className}, {`);
  lines.push(`    decode: SchemaGetter.transform((wire) => ({ _tag: ${JSON.stringify(error.className)}, ...wire })),`);
  lines.push("    encode: SchemaGetter.transform((error) => ({ error: error.error }))");
  lines.push("  })");
  lines.push(")");
  lines.push("");
}

for (const operation of operationErrors) {
  const union = operation.errors.map((error) => `${error.className}Schema`).join(", ");
  lines.push(`export const ${operation.typeName}Schema = ${operation.errors.length === 1 ? union : `Schema.Union([${union}])`}`);
  lines.push(`export type ${operation.typeName} = typeof ${operation.typeName}Schema.Type`);
  lines.push("");
}

while (lines.at(-1) === "") {
  lines.pop();
}

await mkdir(path.dirname(fileURLToPath(outputPath)), { recursive: true });
await writeFile(outputPath, `${lines.join("\n")}\n`);

function codegenPath(envName, fallback) {
  const value = process.env[envName];
  if (value) {
    return pathToFileURL(path.resolve(value));
  }

  return new URL(fallback, repoRoot);
}

function renderSchema(schema, context = {}) {
  if (schema.$ref) {
    return `${refName(schema.$ref)}Schema`;
  }

  if (schema["x-chalk-header"]) {
    const headerSchemaName = knownHeaderSchemaName(schema["x-chalk-header"]);
    if (headerSchemaName) {
      return headerSchemaName;
    }
  }

  if (schema["x-chalk-url-parameter"] && (schema.type === "integer" || schema.type === "number")) {
    return "Schema.NumberFromString";
  }

  if (Array.isArray(schema.type)) {
    const nonNullTypes = schema.type.filter((type) => type !== "null");
    if (nonNullTypes.length === 1 && schema.type.includes("null")) {
      return `Schema.NullOr(${renderSchema({ ...schema, type: nonNullTypes[0] }, context)})`;
    }

    return "Schema.Unknown";
  }

  if (schema.anyOf) {
    const nonNullSchemas = schema.anyOf.filter((member) => member.type !== "null");
    if (nonNullSchemas.length === 1 && nonNullSchemas.length !== schema.anyOf.length) {
      return `Schema.NullOr(${renderSchema(nonNullSchemas[0], context)})`;
    }

    return "Schema.Unknown";
  }

  if (schema.enum) {
    return renderLiteral(schema.enum);
  }

  switch (schema.type) {
    case "object":
      return renderObject(schema, context);
    case "array":
      return renderArray(schema, context);
    case "integer":
    case "number":
      return "Schema.Number";
    case "boolean":
      return "Schema.Boolean";
    case "string":
      return renderString(schema, context);
    case "null":
      return "Schema.Null";
    default:
      return "Schema.Unknown";
  }
}

function knownHeaderSchemaName(headerName) {
  switch (headerName.toLowerCase()) {
    case "retry-after":
      return "RetryAfterHeaderSchema";
    case "x-ratelimit-limit":
      return "RateLimitLimitHeaderSchema";
    case "x-ratelimit-remaining":
      return "RateLimitRemainingHeaderSchema";
    case "location":
      return "LocationHeaderSchema";
    default:
      return "";
  }
}

function renderObject(schema, context) {
  if (schema.additionalProperties && Object.keys(schema.properties ?? {}).length === 0) {
    const valueSchema = schema.additionalProperties === true ? "Schema.Unknown" : renderSchema(schema.additionalProperties, context);
    return `Schema.Record(Schema.String, ${valueSchema})`;
  }

  const required = new Set(schema.required ?? []);
  const entries = Object.entries(schema.properties ?? {}).map(([propertyName, propertySchema]) => {
    const rendered = renderSchema(propertySchema, { ...context, propertyName });
    const field = required.has(propertyName) ? rendered : `Schema.optional(${rendered})`;

    return `  ${JSON.stringify(propertyName)}: ${field}`;
  });

  const fields = `{\n${entries.join(",\n")}\n}`;
  if (schema.additionalProperties) {
    const valueSchema = schema.additionalProperties === true ? "Schema.Unknown" : renderSchema(schema.additionalProperties, context);
    return `Schema.StructWithRest(Schema.Struct(${fields}), [Schema.Record(Schema.String, ${valueSchema})])`;
  }

  return `Schema.Struct(${fields})`;
}

function renderArray(schema, context) {
  const checks = [];
  if (Number.isInteger(schema.minItems)) {
    checks.push(`Schema.isMinLength(${schema.minItems})`);
  }
  if (Number.isInteger(schema.maxItems)) {
    checks.push(`Schema.isMaxLength(${schema.maxItems})`);
  }

  return check(`Schema.Array(${renderSchema(schema.items ?? {}, context)})`, checks);
}

function renderString(schema, context) {
  const checks = [];
  const transforms = [];
  if (Number.isInteger(schema.minLength)) {
    checks.push(`Schema.isMinLength(${schema.minLength})`);
  }
  if (Number.isInteger(schema.maxLength)) {
    checks.push(`Schema.isMaxLength(${schema.maxLength})`);
  }
  if (schema.pattern) {
    checks.push(`Schema.isPattern(${renderRegex(schema.pattern)})`);
  }

  switch (schema.format) {
    case "uuid":
      checks.push("Schema.isPattern(/^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i)");
      break;
    case "date-time":
      checks.push("Schema.isPattern(/^\\d{4}-\\d{2}-\\d{2}T/)");
      break;
    case "email":
      checks.push("Schema.isPattern(/^[^\\s@]+@[^\\s@]+\\.[^\\s@]+$/)");
      break;
    case "uri":
      checks.push("Schema.isPattern(/^[a-z][a-z0-9+.-]*:/i)");
      break;
  }

  if (schema["x-chalk-brand"]) {
    transforms.push(`Schema.brand(${JSON.stringify(schema["x-chalk-brand"])})`);
  }

  return pipe(check("Schema.String", checks), transforms);
}

function check(base, checks) {
  if (checks.length === 0) {
    return base;
  }

  return `${base}.check(${checks.join(", ")})`;
}

function pipe(base, transforms) {
  if (transforms.length === 0) {
    return base;
  }

  return `${base}.pipe(${transforms.join(", ")})`;
}

function renderRegex(pattern) {
  return `new RegExp(${JSON.stringify(pattern)})`;
}

function renderLiteral(values) {
  if (values.length === 1) {
    return `Schema.Literal(${JSON.stringify(values[0])})`;
  }

  return `Schema.Literals([${values.map((value) => JSON.stringify(value)).join(", ")}])`;
}

function collectOperationSchemas(document) {
  const operations = [];
  for (const [, pathItem] of Object.entries(document.paths ?? {})) {
    for (const operation of Object.values(pathItem ?? {})) {
      if (!operation || typeof operation !== "object" || !operation.operationId) {
        continue;
      }

      const operationName = pascalCase(operation.operationId);
      const schemas = [];
      const pathParameters = parameterSchema(operation.parameters, "path");
      const queryParameters = parameterSchema(operation.parameters, "query");
      const requestBody = operation.requestBody?.content?.["application/json"]?.schema;
      const responseBody = successResponseSchema(operation.responses);

      if (pathParameters) {
        schemas.push({ name: `${operationName}PathParams`, schema: pathParameters });
      }
      if (queryParameters) {
        schemas.push({ name: `${operationName}QueryParams`, schema: queryParameters });
      }
      if (requestBody) {
        schemas.push({ name: `${operationName}RequestBody`, schema: requestBody });
      }
      if (responseBody) {
        schemas.push({ name: `${operationName}Response`, schema: responseBody });
      }
      for (const [status, response] of Object.entries(operation.responses ?? {})) {
        const headers = response?.headers ?? {};
        if (Object.keys(headers).length > 0) {
          schemas.push({ name: `${operationName}${status}ResponseHeaders`, schema: headerSchema(headers) });
        }
      }

      if (schemas.length > 0) {
        operations.push({ operationId: operation.operationId, schemas });
      }
    }
  }

  return operations.sort((left, right) => left.operationId.localeCompare(right.operationId));
}

function headerSchema(headers) {
  const required = [];
  const properties = {};
  for (const [name, header] of Object.entries(headers).sort(([left], [right]) => left.localeCompare(right))) {
    if (header.required) {
      required.push(name);
    }
    properties[name] = {
      ...(header.schema ?? {}),
      "x-chalk-header": name,
    };
  }

  return {
    type: "object",
    required,
    properties,
    additionalProperties: false,
  };
}

function collectHeaderDefinitions(operationSchemas) {
  const definitions = new Map();
  for (const operation of operationSchemas) {
    for (const schema of operation.schemas) {
      for (const property of Object.values(schema.schema.properties ?? {})) {
        const headerName = property["x-chalk-header"];
        if (!headerName || definitions.has(headerName)) {
          continue;
        }
        const definition = headerDefinition(headerName, property);
        if (definition) {
          definitions.set(headerName, definition);
        }
      }
    }
  }

  return [...definitions.values()].sort((left, right) => left.typeName.localeCompare(right.typeName));
}

function headerDefinition(headerName, schema) {
  switch (headerName.toLowerCase()) {
    case "retry-after":
      return {
        typeName: "RetryAfterHeader",
        schemaName: "RetryAfterHeaderSchema",
        rendered: "Schema.NumberFromString.check(Schema.isInt(), Schema.isGreaterThanOrEqualTo(1))",
      };
    case "x-ratelimit-limit":
      return {
        typeName: "RateLimitLimitHeader",
        schemaName: "RateLimitLimitHeaderSchema",
        rendered: "Schema.NumberFromString.check(Schema.isInt(), Schema.isGreaterThanOrEqualTo(0))",
      };
    case "x-ratelimit-remaining":
      return {
        typeName: "RateLimitRemainingHeader",
        schemaName: "RateLimitRemainingHeaderSchema",
        rendered: "Schema.NumberFromString.check(Schema.isInt(), Schema.isGreaterThanOrEqualTo(0))",
      };
    case "location":
      return {
        typeName: "LocationHeader",
        schemaName: "LocationHeaderSchema",
        rendered: "URLStringSchema",
      };
    default:
      if (schema.type === "integer" || schema.type === "number") {
        return {
          typeName: `${pascalCase(headerName)}Header`,
          schemaName: `${pascalCase(headerName)}HeaderSchema`,
          rendered: "Schema.NumberFromString",
        };
      }
      return undefined;
  }
}

function parameterSchema(parameters = [], location) {
  const matching = parameters.filter((parameter) => parameter.in === location);
  if (matching.length === 0) {
    return undefined;
  }

  const required = matching
    .filter((parameter) => parameter.required)
    .map((parameter) => parameter.name)
    .sort();
  const properties = Object.fromEntries(
    matching
      .slice()
      .sort((left, right) => left.name.localeCompare(right.name))
      .map((parameter) => [
        parameter.name,
        {
          ...(parameter.schema ?? {}),
          "x-chalk-url-parameter": true,
        },
      ]),
  );

  return {
    type: "object",
    required,
    properties,
    additionalProperties: false,
  };
}

function successResponseSchema(responses = {}) {
  const statuses = Object.keys(responses).sort();
  const status = statuses.find((value) => /^2\d\d$/.test(value)) ?? statuses.find((value) => /^3\d\d$/.test(value));

  if (!status) {
    return undefined;
  }

  return responses[status]?.content?.["application/json"]?.schema;
}

function orderedSchemas(schemas) {
  const ordered = [];
  const visited = new Set();
  const visiting = new Set();

  for (const name of Object.keys(schemas).sort()) {
    visit(name);
  }

  return ordered.map((name) => [name, schemas[name]]);

  function visit(name) {
    if (visited.has(name)) {
      return;
    }
    if (visiting.has(name)) {
      return;
    }

    visiting.add(name);
    for (const dependency of referencedSchemaNames(schemas[name])) {
      if (dependency !== name && schemas[dependency]) {
        visit(dependency);
      }
    }
    visiting.delete(name);
    visited.add(name);
    ordered.push(name);
  }
}

function referencedSchemaNames(schema, names = new Set()) {
  if (!schema || typeof schema !== "object") {
    return names;
  }

  if (schema.$ref) {
    names.add(refName(schema.$ref));
  }

  for (const key of ["anyOf", "oneOf", "allOf"]) {
    for (const member of schema[key] ?? []) {
      referencedSchemaNames(member, names);
    }
  }

  for (const property of Object.values(schema.properties ?? {})) {
    referencedSchemaNames(property, names);
  }

  referencedSchemaNames(schema.items, names);

  if (schema.additionalProperties && schema.additionalProperties !== true) {
    referencedSchemaNames(schema.additionalProperties, names);
  }

  return names;
}

function refName(ref) {
  const prefix = "#/components/schemas/";
  if (!ref.startsWith(prefix)) {
    throw new Error(`Unsupported ref: ${ref}`);
  }

  return ref.slice(prefix.length);
}

function collectOperationErrors(document) {
  const operations = [];
  for (const [, pathItem] of Object.entries(document.paths ?? {})) {
    for (const operation of Object.values(pathItem ?? {})) {
      if (!operation || typeof operation !== "object" || !operation.operationId) {
        continue;
      }

      const errorsByCode = new Map();
      for (const [status, response] of Object.entries(operation.responses ?? {})) {
        for (const code of response["x-chalk-error-codes"] ?? []) {
          if (!errorsByCode.has(code)) {
            errorsByCode.set(code, new Set());
          }
          errorsByCode.get(code).add(Number(status));
        }
      }

      const errors = [...errorsByCode.entries()]
        .map(([code, statuses]) =>
          errorDefinition(
            code,
            [...statuses].sort((a, b) => a - b),
          ),
        )
        .sort((left, right) => left.className.localeCompare(right.className));

      if (errors.length > 0) {
        operations.push({
          typeName: `${pascalCase(operation.operationId)}Error`,
          errors,
        });
      }
    }
  }

  return operations.sort((left, right) => left.typeName.localeCompare(right.typeName));
}

function collectErrorDefinitions(operations) {
  const byCode = new Map();
  for (const operation of operations) {
    for (const error of operation.errors) {
      const existing = byCode.get(error.code);
      if (!existing) {
        byCode.set(error.code, error);
        continue;
      }

      for (const status of error.statuses) {
        if (!existing.statuses.includes(status)) {
          existing.statuses.push(status);
        }
      }
      existing.statuses.sort((a, b) => a - b);
    }
  }

  return [...byCode.values()].sort((left, right) => left.className.localeCompare(right.className));
}

function errorDefinition(code, statuses) {
  const name = errorClassName(code);
  return {
    code,
    statuses,
    className: name,
  };
}

function errorClassName(code) {
  const name =
    {
      internal_error: "InternalError",
    }[code] ?? pascalCase(code);

  return name.endsWith("Error") ? name : `${name}Error`;
}

function pascalCase(value) {
  return value
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part) => `${part.slice(0, 1).toUpperCase()}${part.slice(1)}`)
    .join("");
}
