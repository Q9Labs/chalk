// @ts-check

import { execFile } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { promisify } from "node:util";
import { codegenPath, loadSyncContract, pascalCase, stableJson, syncProtocolVersion } from "./sync-contract.mjs";

const execute = promisify(execFile);
const protocolVersion = syncProtocolVersion();
const outputPath = codegenPath("CODEGEN_SYNC_TYPESCRIPT_OUTPUT_PATH", protocolVersion === 1 ? "sdks/typescript/client/src/generated/sync.ts" : "sdks/typescript/client/src/generated/sync-v2.ts");
const contract = await loadSyncContract(undefined, protocolVersion);
const source = contract.version === 1 ? renderTypeScript(contract) : renderV2TypeScript(contract);

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, source);
await execute("pnpm", ["exec", "oxfmt", "--write", outputPath]);

/**
 * @param {Record<string, any>} contract
 */
function renderTypeScript(contract) {
  const lines = [
    "// Generated by tools/contract-codegen/src/emitters/sync-typescript.mjs",
    "// Do not edit by hand.",
    'import { Schema } from "effect";',
    "",
    `export const SyncProtocolVersion = ${contract.protocol.value} as const;`,
    `export const SyncProtocolMetadata = ${stableJson({
      version: contract.version,
      protocol: contract.protocol,
      directions: contract.directions,
      phases: contract.phases,
      continuity: contract.continuity,
      idempotency: contract.idempotency,
      closeCodes: contract.closeCodes,
      errorConnection: contract.error.connection,
    })} as const;`,
    "",
    "export const SyncCloseCodes = SyncProtocolMetadata.closeCodes;",
    "",
  ];

  lines.push(`export const ParticipantSchema = ${renderParticipantSchema(contract.welcome.modes)};`);
  lines.push("export type Participant = typeof ParticipantSchema.Type;");
  lines.push("");
  lines.push(`export const SnapshotSchema = ${renderSnapshotSchema(contract.welcome.modes)};`);
  lines.push("export type Snapshot = typeof SnapshotSchema.Type;");
  lines.push("");
  lines.push(`export const HelloFrameSchema = ${renderHelloSchema(contract.hello, contract.protocol.value)};`);
  lines.push("export type HelloFrame = typeof HelloFrameSchema.Type;");
  lines.push("");

  for (const event of contract.events) {
    const name = `${pascalCase(event.name)}EventFrame`;
    lines.push(`export const ${name}Schema = ${renderEventSchema(event)};`);
    lines.push(`export type ${name} = typeof ${name}Schema.Type;`);
    lines.push("");
  }

  lines.push(`export const EventFrameSchema = ${renderUnion(contract.events.map((event) => `${pascalCase(event.name)}EventFrameSchema`))};`);
  lines.push("export type EventFrame = typeof EventFrameSchema.Type;");
  lines.push("");

  for (const mode of contract.welcome.modes) {
    const name = `Welcome${pascalCase(mode.id)}Frame`;
    lines.push(`export const ${name}Schema = ${renderWelcomeSchema(contract, mode)};`);
    lines.push(`export type ${name} = typeof ${name}Schema.Type;`);
    lines.push("");
  }

  for (const command of contract.commands) {
    const name = `${pascalCase(command.name)}CommandFrame`;
    lines.push(`export const ${name}Schema = ${renderCommandSchema(command)};`);
    lines.push(`export type ${name} = typeof ${name}Schema.Type;`);
    lines.push("");
  }

  for (const ack of contract.acks) {
    const name = `${pascalCase(ack.result)}AckFrame`;
    lines.push(`export const ${name}Schema = ${renderAckSchema(ack)};`);
    lines.push(`export type ${name} = typeof ${name}Schema.Type;`);
    lines.push("");
  }

  lines.push(`export const ErrorFrameSchema = ${renderErrorSchema(contract.error)};`);
  lines.push("export type ErrorFrame = typeof ErrorFrameSchema.Type;");
  lines.push("");
  lines.push(`export const PingFrameSchema = Schema.Struct({ type: Schema.Literal(${JSON.stringify(contract.ping.type)}) });`);
  lines.push("export type PingFrame = typeof PingFrameSchema.Type;");
  lines.push("");
  lines.push(`export const PongFrameSchema = Schema.Struct({ type: Schema.Literal(${JSON.stringify(contract.pong.type)}) });`);
  lines.push("export type PongFrame = typeof PongFrameSchema.Type;");
  lines.push("");
  lines.push(`export const ClientFrameSchema = ${renderUnion(["HelloFrameSchema", ...contract.commands.map((command) => `${pascalCase(command.name)}CommandFrameSchema`), "PingFrameSchema"])};`);
  lines.push("export type ClientFrame = typeof ClientFrameSchema.Type;");
  lines.push("");
  lines.push(`export const ServerFrameSchema = ${renderUnion([...contract.welcome.modes.map((mode) => `Welcome${pascalCase(mode.id)}FrameSchema`), "EventFrameSchema", ...contract.acks.map((ack) => `${pascalCase(ack.result)}AckFrameSchema`), "ErrorFrameSchema", "PongFrameSchema"])};`);
  lines.push("export type ServerFrame = typeof ServerFrameSchema.Type;");

  return `${lines.join("\n")}\n`;
}

/**
 * @param {Record<string, any>} contract
 */
function renderV2TypeScript(contract) {
  const metadata = {
    version: contract.version,
    protocol: contract.protocol,
    directions: contract.directions,
    phases: contract.phases,
    limits: contract.limits,
    continuity: contract.continuity,
    idempotency: contract.idempotency,
    closeCodes: contract.closeCodes,
  };
  const limits = contract.limits;
  const lines = [
    "// Generated by tools/contract-codegen/src/emitters/sync-typescript.mjs",
    "// Do not edit by hand.",
    'import { Schema } from "effect";',
    "",
    "export const SyncProtocolVersion = 2 as const;",
    "export const SyncProtocolMetadata = " + stableJson(metadata) + " as const;",
    "export const SyncProtocolLimits = SyncProtocolMetadata.limits;",
    "export const SyncCloseCodes = SyncProtocolMetadata.closeCodes;",
    "export const SyncStateDigestMetadata = " + stableJson(contract.stateDigest) + " as const;",
    "",
    "const textEncoder = new TextEncoder();",
    "",
    "export function encodedSyncFrameBytes(frame: unknown): number {",
    "  return textEncoder.encode(JSON.stringify(frame)).byteLength;",
    "}",
    "",
    "function boundedUtf8String(maxBytes: number) {",
    "  return Schema.String.check(Schema.makeFilter((value) =>",
    "    textEncoder.encode(value).byteLength <= maxBytes ? undefined : { path: [], issue: 'must fit the UTF-8 byte limit' },",
    "  ));",
    "}",
    "",
    "function exactKeys(keys: readonly string[]) {",
    "  return Schema.makeFilter((value) =>",
    "    typeof value === 'object' && value !== null && !Array.isArray(value) && Object.keys(value).length === keys.length && keys.every((key) => Object.hasOwn(value, key))",
    "      ? undefined",
    "      : { path: [], issue: 'must not contain unknown or missing fields' },",
    "  );",
    "}",
    "",
    "function boundedFrame(maxBytes: number) {",
    "  return Schema.makeFilter((frame) =>",
    "    encodedSyncFrameBytes(frame) <= maxBytes ? undefined : { path: [], issue: 'must fit the encoded byte limit' },",
    "  );",
    "}",
    "",
    "function isObject(value: unknown): value is Record<string, unknown> {",
    "  return typeof value === 'object' && value !== null && !Array.isArray(value);",
    "}",
    "",
    "function exactObject(value: unknown, keys: readonly string[]): value is Record<string, unknown> {",
    "  return isObject(value) && Object.keys(value).length === keys.length && keys.every((key) => Object.hasOwn(value, key));",
    "}",
    "",
    "function strictCursor(value: unknown): boolean {",
    "  return exactObject(value, ['revision', 'state_schema_version', 'state_digest']);",
    "}",
    "",
    "function strictSnapshot(value: unknown): boolean {",
    "  return exactObject(value, ['control_revision', 'state_schema_version', 'state_digest', 'status', 'participants']) &&",
    "    Array.isArray(value.participants) &&",
    "    value.participants.every((participant) => exactObject(participant, ['participant_session_id', 'display_name', 'hand_raised']));",
    "}",
    "",
    "function strictEvent(value: unknown): boolean {",
    "  if (!isObject(value) || value.type !== 'event' || value.stream !== 'control' || !isObject(value.payload)) return false;",
    "  const common = ['type', 'stream', 'name', 'event_id', 'base_revision', 'revision', 'schema_version', 'resulting_state_digest', 'payload'];",
    "  switch (value.name) {",
    "    case 'participant_joined':",
    "      return exactObject(value, [...common, 'lifecycle_intent_id']) && exactObject(value.payload, ['display_name', 'participant_session_id']);",
    "    case 'participant_left':",
    "      return exactObject(value, [...common, 'lifecycle_intent_id']) && exactObject(value.payload, ['participant_session_id']);",
    "    case 'session_ended':",
    "      return exactObject(value, [...common, 'lifecycle_intent_id']) && exactObject(value.payload, []);",
    "    case 'hand_raised':",
    "    case 'hand_lowered':",
    "      return exactObject(value, [...common, 'command_id']) && exactObject(value.payload, ['participant_session_id']);",
    "    default:",
    "      return false;",
    "  }",
    "}",
    "",
    "function strictWelcome(value: Record<string, unknown>): boolean {",
    "  const base = ['type', 'protocol', 'participant_session_id', 'participant_session_generation', 'recovery_id', 'head', 'mode'];",
    "  if (!strictCursor(value.head)) return false;",
    "  switch (value.mode) {",
    "    case 'snapshot': return exactObject(value, [...base, 'snapshot']) && strictSnapshot(value.snapshot);",
    "    case 'replay':",
    "    case 'up_to_date': return exactObject(value, base);",
    "    case 'terminal': return exactObject(value, [...base, 'reason']);",
    "    default: return false;",
    "  }",
    "}",
    "",
    "function strictClientFrame(value: unknown): boolean {",
    "  if (!isObject(value)) return false;",
    "  switch (value.type) {",
    "    case 'hello': return exactObject(value, ['type', 'protocol', 'token', 'streams']) && exactObject(value.streams, ['control']) && isObject(value.streams.control) && exactObject(value.streams.control, ['cursor']) && (value.streams.control.cursor === null || strictCursor(value.streams.control.cursor));",
    "    case 'command': return exactObject(value, ['type', 'command_id', 'name', 'payload']) && exactObject(value.payload, []);",
    "    case 'delivery_ack': return exactObject(value, ['type', 'stream', 'revision', 'state_digest']);",
    "    case 'recovery_ack': return exactObject(value, ['type', 'recovery_id', 'revision', 'state_digest']);",
    "    case 'ping': return exactObject(value, ['type']);",
    "    default: return false;",
    "  }",
    "}",
    "",
    "function strictServerFrame(value: unknown): boolean {",
    "  if (!isObject(value)) return false;",
    "  switch (value.type) {",
    "    case 'welcome': return strictWelcome(value);",
    "    case 'replay_page': return exactObject(value, ['type', 'recovery_id', 'first_revision', 'last_revision', 'events']) && Array.isArray(value.events) && value.events.every(strictEvent);",
    "    case 'recovery_complete': return exactObject(value, ['type', 'recovery_id', 'head']) && strictCursor(value.head);",
    "    case 'event': return strictEvent(value);",
    "    case 'ack':",
    "      return value.result === 'rejected'",
    "        ? exactObject(value, ['type', 'command_id', 'result', 'reason'])",
    "        : (value.result === 'committed' || value.result === 'duplicate') && exactObject(value, ['type', 'command_id', 'result', 'event_id', 'revision']);",
    "    case 'retryable_error': return exactObject(value, ['type', 'command_id', 'code']);",
    "    case 'error': return exactObject(value, ['type', 'code', 'detail']);",
    "    case 'pong': return exactObject(value, ['type']);",
    "    default: return false;",
    "  }",
    "}",
    "",
    "export const NonNegativeIntegerSchema = Schema.Number.check(Schema.isInt(), Schema.isGreaterThanOrEqualTo(0));",
    "export const PositiveIntegerSchema = Schema.Number.check(Schema.isInt(), Schema.isGreaterThanOrEqualTo(1));",
    "export const UuidSchema = Schema.String.check(Schema.makeFilter((value) =>",
    "  /^[0-9a-f]{8}-[0-9a-f]{4}-[1-8][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/iu.test(value)",
    "    ? undefined",
    "    : { path: [], issue: 'must be an RFC 4122 UUID' },",
    "));",
    "export const StateDigestSchema = Schema.String.check(Schema.makeFilter((value) =>",
    "  /^[0-9a-f]{64}$/u.test(value) ? undefined : { path: [], issue: 'must be a lowercase hexadecimal SHA-256 digest' },",
    "));",
    "export const CommandIdSchema = boundedUtf8String(" + limits.commandIdMaxBytes + ").check(Schema.makeFilter((value) =>",
    "  textEncoder.encode(value).byteLength >= " + limits.commandIdMinBytes,
    "    ? undefined",
    "    : { path: [], issue: 'must meet the command ID minimum byte length' },",
    "));",
    "",
    "export const ControlCursorSchema = Schema.Struct({",
    "  revision: NonNegativeIntegerSchema,",
    "  state_schema_version: PositiveIntegerSchema,",
    "  state_digest: StateDigestSchema,",
    "}).check(exactKeys(['revision', 'state_schema_version', 'state_digest']));",
    "export type ControlCursor = typeof ControlCursorSchema.Type;",
    "",
    'export const TerminalRecoveryReasonSchema = Schema.Union([Schema.Literal("session_ended"), Schema.Literal("participant_inactive"), Schema.Literal("stale_participant_generation")]);',
    "export type TerminalRecoveryReason = typeof TerminalRecoveryReasonSchema.Type;",
    'export const RejectionReasonSchema = Schema.Union([Schema.Literal("session_ended"), Schema.Literal("participant_inactive"), Schema.Literal("stale_participant_generation"), Schema.Literal("capability_denied"), Schema.Literal("invalid_state"), Schema.Literal("command_id_conflict")]);',
    "export type RejectionReason = typeof RejectionReasonSchema.Type;",
    'export const RetryableErrorCodeSchema = Schema.Union([Schema.Literal("overloaded"), Schema.Literal("server_draining"), Schema.Literal("dependency_unavailable"), Schema.Literal("decision_unavailable")]);',
    "export type RetryableErrorCode = typeof RetryableErrorCodeSchema.Type;",
    'export const ProtocolErrorCodeSchema = Schema.Union([Schema.Literal("protocol_error"), Schema.Literal("invalid_frame"), Schema.Literal("unsupported_protocol")]);',
    "export type ProtocolErrorCode = typeof ProtocolErrorCodeSchema.Type;",
    "",
    "export const ParticipantSchema = Schema.Struct({",
    "  participant_session_id: UuidSchema,",
    "  display_name: boundedUtf8String(256),",
    "  hand_raised: Schema.Boolean,",
    "}).check(exactKeys(['participant_session_id', 'display_name', 'hand_raised']));",
    "export type Participant = typeof ParticipantSchema.Type;",
    "",
    "export const SnapshotSchema = Schema.Struct({",
    "  control_revision: NonNegativeIntegerSchema,",
    "  state_schema_version: PositiveIntegerSchema,",
    "  state_digest: StateDigestSchema,",
    '  status: Schema.Union([Schema.Literal("active"), Schema.Literal("ended")]),',
    "  participants: Schema.Array(ParticipantSchema).check(Schema.isMaxLength(500)),",
    "}).check(",
    "  exactKeys(['control_revision', 'state_schema_version', 'state_digest', 'status', 'participants']),",
    "  boundedFrame(" + limits.snapshotEncodedBytes + "),",
    ");",
    "export type Snapshot = typeof SnapshotSchema.Type;",
    "",
    "const ControlStreamSchema = Schema.Struct({ cursor: Schema.NullOr(ControlCursorSchema) }).check(exactKeys(['cursor']));",
    "const StreamsSchema = Schema.Struct({ control: ControlStreamSchema }).check(exactKeys(['control']));",
    "export const HelloFrameSchema = Schema.Struct({",
    '  type: Schema.Literal("hello"),',
    "  protocol: Schema.Literal(2),",
    "  token: boundedUtf8String(" + limits.tokenBytes + "),",
    "  streams: StreamsSchema,",
    "}).check(exactKeys(['type', 'protocol', 'token', 'streams']), boundedFrame(" + limits.decodedInboundFrameBytes + "));",
    "export type HelloFrame = typeof HelloFrameSchema.Type;",
    "",
    "export const DeliveryAckFrameSchema = Schema.Struct({",
    "  type: Schema.Literal('delivery_ack'),",
    "  stream: Schema.Literal('control'),",
    "  revision: PositiveIntegerSchema,",
    "  state_digest: StateDigestSchema,",
    "}).check(exactKeys(['type', 'stream', 'revision', 'state_digest']), boundedFrame(" + limits.decodedInboundFrameBytes + "));",
    "export type DeliveryAckFrame = typeof DeliveryAckFrameSchema.Type;",
    "",
    "export const RecoveryAckFrameSchema = Schema.Struct({",
    "  type: Schema.Literal('recovery_ack'),",
    "  recovery_id: UuidSchema,",
    "  revision: NonNegativeIntegerSchema,",
    "  state_digest: StateDigestSchema,",
    "}).check(exactKeys(['type', 'recovery_id', 'revision', 'state_digest']), boundedFrame(" + limits.decodedInboundFrameBytes + "));",
    "export type RecoveryAckFrame = typeof RecoveryAckFrameSchema.Type;",
    "",
    ...renderV2EventSchemas(limits.encodedLiveEventBytes),
    "",
    "export const EventFrameSchema = Schema.Union([ParticipantJoinedEventFrameSchema, ParticipantLeftEventFrameSchema, SessionEndedEventFrameSchema, HandRaisedEventFrameSchema, HandLoweredEventFrameSchema]);",
    "export type EventFrame = typeof EventFrameSchema.Type;",
    "",
    "const WelcomeFrameBase = {",
    '  type: Schema.Literal("welcome"),',
    "  protocol: Schema.Literal(2),",
    "  participant_session_id: UuidSchema,",
    "  participant_session_generation: PositiveIntegerSchema,",
    "  recovery_id: UuidSchema,",
    "  head: ControlCursorSchema,",
    "};",
    "export const WelcomeSnapshotFrameSchema = Schema.Struct({ ...WelcomeFrameBase, mode: Schema.Literal('snapshot'), snapshot: SnapshotSchema }).check(",
    "  exactKeys(['type', 'protocol', 'participant_session_id', 'participant_session_generation', 'recovery_id', 'head', 'mode', 'snapshot']),",
    "  boundedFrame(" + limits.snapshotEncodedBytes + "),",
    ");",
    "export type WelcomeSnapshotFrame = typeof WelcomeSnapshotFrameSchema.Type;",
    "export const WelcomeReplayFrameSchema = Schema.Struct({ ...WelcomeFrameBase, mode: Schema.Literal('replay') }).check(",
    "  exactKeys(['type', 'protocol', 'participant_session_id', 'participant_session_generation', 'recovery_id', 'head', 'mode']),",
    ");",
    "export type WelcomeReplayFrame = typeof WelcomeReplayFrameSchema.Type;",
    "export const WelcomeUpToDateFrameSchema = Schema.Struct({ ...WelcomeFrameBase, mode: Schema.Literal('up_to_date') }).check(",
    "  exactKeys(['type', 'protocol', 'participant_session_id', 'participant_session_generation', 'recovery_id', 'head', 'mode']),",
    ");",
    "export type WelcomeUpToDateFrame = typeof WelcomeUpToDateFrameSchema.Type;",
    "export const WelcomeTerminalFrameSchema = Schema.Struct({ ...WelcomeFrameBase, mode: Schema.Literal('terminal'), reason: TerminalRecoveryReasonSchema }).check(",
    "  exactKeys(['type', 'protocol', 'participant_session_id', 'participant_session_generation', 'recovery_id', 'head', 'mode', 'reason']),",
    ");",
    "export type WelcomeTerminalFrame = typeof WelcomeTerminalFrameSchema.Type;",
    "export const WelcomeFrameSchema = Schema.Union([WelcomeSnapshotFrameSchema, WelcomeReplayFrameSchema, WelcomeUpToDateFrameSchema, WelcomeTerminalFrameSchema]);",
    "export type WelcomeFrame = typeof WelcomeFrameSchema.Type;",
    "",
    "export const ReplayPageFrameSchema = Schema.Struct({",
    '  type: Schema.Literal("replay_page"),',
    "  recovery_id: UuidSchema,",
    "  first_revision: PositiveIntegerSchema,",
    "  last_revision: PositiveIntegerSchema,",
    "  events: Schema.Array(EventFrameSchema).check(Schema.isMaxLength(" + limits.replayPageMaxEvents + ")),",
    "}).check(",
    "  exactKeys(['type', 'recovery_id', 'first_revision', 'last_revision', 'events']),",
    "  Schema.makeFilter((frame) => {",
    "    const [first] = frame.events;",
    "    const last = frame.events.at(-1);",
    "    const contiguous = frame.events.every((event, index) => index === 0 || event.base_revision === frame.events[index - 1]!.revision);",
    "    return encodedSyncFrameBytes(frame) <= SyncProtocolLimits.replayPageEncodedBytes && first !== undefined && last !== undefined && first.revision === frame.first_revision && last.revision === frame.last_revision && contiguous",
    "      ? undefined",
    "      : { path: [], issue: 'must declare one bounded contiguous page with exact first_revision and last_revision' };",
    "  }),",
    ");",
    "export type ReplayPageFrame = typeof ReplayPageFrameSchema.Type;",
    "export const RecoveryCompleteFrameSchema = Schema.Struct({ type: Schema.Literal('recovery_complete'), recovery_id: UuidSchema, head: ControlCursorSchema }).check(",
    "  exactKeys(['type', 'recovery_id', 'head']),",
    ");",
    "export type RecoveryCompleteFrame = typeof RecoveryCompleteFrameSchema.Type;",
    "",
    ...renderV2CommandSchemas(),
    "",
    "export const CommittedAckFrameSchema = Schema.Struct({ type: Schema.Literal('ack'), command_id: CommandIdSchema, result: Schema.Literal('committed'), event_id: UuidSchema, revision: PositiveIntegerSchema }).check(",
    "  exactKeys(['type', 'command_id', 'result', 'event_id', 'revision']),",
    ");",
    "export type CommittedAckFrame = typeof CommittedAckFrameSchema.Type;",
    "export const DuplicateAckFrameSchema = Schema.Struct({ type: Schema.Literal('ack'), command_id: CommandIdSchema, result: Schema.Literal('duplicate'), event_id: UuidSchema, revision: PositiveIntegerSchema }).check(",
    "  exactKeys(['type', 'command_id', 'result', 'event_id', 'revision']),",
    ");",
    "export type DuplicateAckFrame = typeof DuplicateAckFrameSchema.Type;",
    "export const RejectedAckFrameSchema = Schema.Struct({ type: Schema.Literal('ack'), command_id: CommandIdSchema, result: Schema.Literal('rejected'), reason: RejectionReasonSchema }).check(",
    "  exactKeys(['type', 'command_id', 'result', 'reason']),",
    ");",
    "export type RejectedAckFrame = typeof RejectedAckFrameSchema.Type;",
    "",
    "export const RetryableErrorFrameSchema = Schema.Struct({ type: Schema.Literal('retryable_error'), command_id: CommandIdSchema, code: RetryableErrorCodeSchema }).check(",
    "  exactKeys(['type', 'command_id', 'code']),",
    ");",
    "export type RetryableErrorFrame = typeof RetryableErrorFrameSchema.Type;",
    "export const ErrorFrameSchema = Schema.Struct({ type: Schema.Literal('error'), code: ProtocolErrorCodeSchema, detail: boundedUtf8String(" + limits.protocolErrorDetailBytes + ") }).check(",
    "  exactKeys(['type', 'code', 'detail']),",
    ");",
    "export type ErrorFrame = typeof ErrorFrameSchema.Type;",
    "export const PingFrameSchema = Schema.Struct({ type: Schema.Literal('ping') }).check(exactKeys(['type']));",
    "export type PingFrame = typeof PingFrameSchema.Type;",
    "export const PongFrameSchema = Schema.Struct({ type: Schema.Literal('pong') }).check(exactKeys(['type']));",
    "export type PongFrame = typeof PongFrameSchema.Type;",
    "",
    "export const ClientFrameSchema = Schema.Unknown.check(Schema.makeFilter((frame) => strictClientFrame(frame) ? undefined : { path: [], issue: 'must not contain unknown or missing fields' })).pipe(Schema.decodeTo(Schema.Union([HelloFrameSchema, RaiseHandCommandFrameSchema, LowerHandCommandFrameSchema, DeliveryAckFrameSchema, RecoveryAckFrameSchema, PingFrameSchema]))).check(boundedFrame(SyncProtocolLimits.decodedInboundFrameBytes));",
    "export type ClientFrame = typeof ClientFrameSchema.Type;",
    "export const ServerFrameSchema = Schema.Unknown.check(Schema.makeFilter((frame) => strictServerFrame(frame) ? undefined : { path: [], issue: 'must not contain unknown or missing fields' })).pipe(Schema.decodeTo(Schema.Union([WelcomeFrameSchema, ReplayPageFrameSchema, RecoveryCompleteFrameSchema, EventFrameSchema, CommittedAckFrameSchema, DuplicateAckFrameSchema, RejectedAckFrameSchema, RetryableErrorFrameSchema, ErrorFrameSchema, PongFrameSchema])));",
    "export type ServerFrame = typeof ServerFrameSchema.Type;",
    "",
    "export function encodeSyncFrame(frame: ClientFrame | ServerFrame): string {",
    "  return JSON.stringify(frame);",
    "}",
  ];
  return lines.join("\n") + "\n";
}

/**
 * @param {number} encodedLiveEventBytes
 */
function renderV2EventSchemas(encodedLiveEventBytes) {
  return [
    ...renderV2EventSchema("ParticipantJoined", "participant_joined", "Schema.Struct({ display_name: boundedUtf8String(256), participant_session_id: UuidSchema }).check(exactKeys(['display_name', 'participant_session_id']))", "lifecycle_intent_id", encodedLiveEventBytes),
    "",
    ...renderV2EventSchema("ParticipantLeft", "participant_left", "Schema.Struct({ participant_session_id: UuidSchema }).check(exactKeys(['participant_session_id']))", "lifecycle_intent_id", encodedLiveEventBytes),
    "",
    ...renderV2EventSchema("SessionEnded", "session_ended", "Schema.Struct({}).check(exactKeys([]))", "lifecycle_intent_id", encodedLiveEventBytes),
    "",
    ...renderV2EventSchema("HandRaised", "hand_raised", "Schema.Struct({ participant_session_id: UuidSchema }).check(exactKeys(['participant_session_id']))", "command_id", encodedLiveEventBytes),
    "",
    ...renderV2EventSchema("HandLowered", "hand_lowered", "Schema.Struct({ participant_session_id: UuidSchema }).check(exactKeys(['participant_session_id']))", "command_id", encodedLiveEventBytes),
  ];
}

/**
 * @param {string} typeName
 * @param {string} eventName
 * @param {string} payload
 * @param {string} origin
 * @param {number} encodedLiveEventBytes
 */
function renderV2EventSchema(typeName, eventName, payload, origin, encodedLiveEventBytes) {
  const keys = ["type", "stream", "name", "event_id", "base_revision", "revision", "schema_version", "resulting_state_digest", "payload", origin];
  const originSchema = origin === "command_id" ? "CommandIdSchema" : "UuidSchema";
  return [
    "export const " + typeName + "EventFrameSchema = Schema.Struct({",
    "  type: Schema.Literal('event'),",
    "  stream: Schema.Literal('control'),",
    "  name: Schema.Literal('" + eventName + "'),",
    "  event_id: UuidSchema,",
    "  base_revision: NonNegativeIntegerSchema,",
    "  revision: PositiveIntegerSchema,",
    "  schema_version: PositiveIntegerSchema,",
    "  resulting_state_digest: StateDigestSchema,",
    "  payload: " + payload + ",",
    "  " + origin + ": " + originSchema + ",",
    "}).check(",
    "  exactKeys(" + JSON.stringify(keys) + "),",
    "  Schema.makeFilter((frame) =>",
    "    encodedSyncFrameBytes(frame) <= " + encodedLiveEventBytes + " && frame.revision === frame.base_revision + 1",
    "      ? undefined",
    "      : { path: [], issue: 'must have bounded encoded size and revision equal to base_revision + 1' },",
    "  ),",
    ");",
    "export type " + typeName + "EventFrame = typeof " + typeName + "EventFrameSchema.Type;",
  ];
}

