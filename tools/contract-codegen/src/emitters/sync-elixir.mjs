/** @param {Record<string, any>[]} commands */
function renderV3ElixirCommandPayloads(commands) {
  return commands.flatMap((command) => {
    const fields = Object.entries(command.payload).map(([name, field]) => [snakeCase(name), field]);
    const pattern = fields.map(([name]) => `${JSON.stringify(name)} => ${name}`).join(", ");
    const keys = fields.map(([name]) => name);
    const checks = fields.map(([name, field]) => renderV3ElixirFieldCheck(field, name)).join(" and ");
    return [`  defp valid_command_payload?(${JSON.stringify(command.name)}, %{${pattern}} = payload) do`, `    exact_keys?(payload, ${renderElixirValue(keys)}) and ${checks}`, "  end", ""];
  });
}

/** @param {Record<string, any>[]} operations */
function renderV3ElixirOperationPayloads(operations) {
  return operations.flatMap((operation) => {
    const fields = Object.keys(operation.payload).map(snakeCase);
    const pattern = fields.map((name) => `${JSON.stringify(name)} => ${name}`).join(", ");
    const checks = fields.length === 0 ? "true" : fields.map((name) => `valid_uuid?(${name})`).join(" and ");
    return [`  defp valid_operation_payload?(${JSON.stringify(operation.name)}, %{${pattern}} = payload) do`, `    exact_keys?(payload, ${renderElixirValue(fields)}) and ${checks}`, "  end", ""];
  });
}

/** @param {Record<string, any>[]} events */
function renderV3ElixirEventPayloads(events) {
  return events.flatMap((event) => {
    const fields = Object.entries(event.payload).map(([name, field]) => [snakeCase(name), field]);
    const pattern = fields.map(([name]) => `${JSON.stringify(name)} => ${name}`).join(", ");
    const keys = fields.map(([name]) => name);
    const checks = fields.map(([name, field]) => renderV3ElixirFieldCheck(field, name)).join(" and ");
    return [`  defp valid_event_payload?(${JSON.stringify(event.name)}, %{${pattern}} = payload) do`, `    exact_keys?(payload, ${renderElixirValue(keys)}) and ${checks}`, "  end", ""];
  });
}

/** @param {Record<string, any>} field @param {string} variable */
function renderV3ElixirFieldCheck(field, variable) {
  if (field === "uuid") return `valid_uuid?(${variable})`;
  if (Array.isArray(field)) return `${variable} in ${renderElixirValue(field)}`;
  if (field.kind === "boolean") return `is_boolean(${variable})`;
  if (field.kind === "integer") return field.minimum > 0 ? `valid_positive_integer?(${variable})` : `valid_non_negative_integer?(${variable})`;
  if (field.kind === "array") {
    const values = field.of === "role" ? "@roles" : "@capabilities";
    return `is_list(${variable}) and length(${variable}) <= ${field.maxItems} and Enum.all?(${variable}, &(&1 in ${values}))`;
  }
  if (field.format === "uuid") return `valid_uuid?(${variable})`;
  if (field.enum) return `${variable} in ${renderElixirValue(field.enum)}`;
  const minimum = field.minBytes ?? 0;
  const trimmed = field.trimmedNonEmpty ? ` and ${variable} != "" and ${variable} == String.trim(${variable})` : "";
  const check = `bounded_string?(${variable}, ${minimum}, ${field.maxBytes})${trimmed}`;
  return field.nullable ? `(is_nil(${variable}) or ${check})` : check;
}

/**
 * @param {Record<string, any>} event
 */
function renderEventClause(event) {
  const payloadChecks = Object.entries(event.payload)
    .map(([name, field]) => `${elixirPredicate(field, nameToVariable(name))}`)
    .join(" and ");
  const payloadPattern = Object.keys(event.payload)
    .map((name) => `${inspectString(snakeCase(name))} => ${nameToVariable(name)}`)
    .join(", ");

  return `  defp valid_event_frame?(%{"type" => ${inspectString(event.type)}, "stream" => ${inspectString(event.stream)}, "name" => ${inspectString(event.name)}, "base_revision" => base_revision, "revision" => revision, "payload" => %{${payloadPattern}}} = frame) do
    valid_correlation_fields?(frame) and valid_revision_pair?(base_revision, revision) and ${payloadChecks}
  end`;
}

/**
 * @param {Record<string, any>} ack
 */
function renderAckClause(ack) {
  const extra = ack.revision ? ` and valid_positive_integer?(revision)` : " and is_binary(reason)";
  const field = ack.revision ? `"revision" => revision` : `"reason" => reason`;
  return `  defp valid_server_frame_body?(%{
        "type" => ${inspectString(ack.type)},
        "command_id" => command_id,
        "result" => ${inspectString(ack.result)},
        ${field}
      }),
      do: is_binary(command_id)${extra}`;
}

/**
 * @param {unknown} value
 */
function renderElixirValue(value) {
  if (value === null) {
    return "nil";
  }
  if (typeof value === "string") {
    return inspectString(value);
  }
  if (typeof value === "number") {
    const rendered = String(value);
    if (Number.isInteger(value) && Math.abs(value) >= 10_000) {
      const sign = rendered.startsWith("-") ? "-" : "";
      const digits = sign ? rendered.slice(1) : rendered;
      return sign + digits.replace(/\B(?=(\d{3})+(?!\d))/gu, "_");
    }
    return rendered;
  }
  if (typeof value === "boolean") {
    return String(value);
  }
  if (Array.isArray(value)) {
    return `[${value.map(renderElixirValue).join(", ")}]`;
  }
  return `%{${Object.entries(value)
    .map(([key, child]) => `${inspectString(key)} => ${renderElixirValue(child)}`)
    .join(", ")}}`;
}

/**
 * @param {Record<string, any>[]} closeCodes
 */
function renderCloseCodes(closeCodes) {
  return `%{${closeCodes.map((closeCode) => `${closeCode.code} => ${renderElixirValue(closeCode)}`).join(", ")}}`;
}

/**
 * @param {string} value
 */
function inspectString(value) {
  return JSON.stringify(value);
}

/**
 * @param {string} value
 */
function snakeCase(value) {
  return value.replace(/[A-Z]/gu, (letter) => `_${letter.toLowerCase()}`);
}

/**
 * @param {string} value
 */
function nameToVariable(value) {
  return snakeCase(value);
}

/**
 * @param {Record<string, any>} field
 * @param {string} variable
 */
function elixirPredicate(field, variable) {
  switch (field.kind) {
    case "boolean":
      return `is_boolean(${variable})`;
    case "integer":
      return field.minimum > 0 ? `valid_positive_integer?(${variable})` : `valid_non_negative_integer?(${variable})`;
    default:
      return `is_binary(${variable})`;
  }
}

/**
 * @param {Record<string, any>} fields
 */
function renderCorrelationType(fields) {
  return `%{${renderCorrelationTypeEntries(fields)}}`;
}

/**
 * @param {Record<string, any>} fields
 */
function renderCorrelationTypeEntries(fields) {
  return Object.entries(fields)
    .map(([name, field]) => `optional(:${inspectString(name)}) => ${elixirType(field)}`)
    .join(", ");
}

/**
 * @param {Record<string, any>} fields
 */
function renderCorrelationFieldKeys(fields) {
  return `%{${Object.keys(fields)
    .map((name) => `${inspectString(name)} => :${inspectString(name)}`)
    .join(", ")}}`;
}

/**
 * @param {Record<string, any>} fields
 */
function renderCorrelationValidation(fields) {
  const checks = Object.entries(fields).map(([name, field]) => `(not Map.has_key?(frame, ${inspectString(name)}) or ${elixirPredicate(field, `frame[${inspectString(name)}]`)})`);
  return checks.join(" and ") || "true";
}

/**
 * @param {Record<string, any>} field
 */
function elixirType(field) {
  switch (field.kind) {
    case "boolean":
      return "boolean()";
    case "integer":
      return field.minimum > 0 ? "pos_integer()" : "non_neg_integer()";
    case "array":
      return "list()";
    case "object":
      return "map()";
    default:
      return "String.t()";
  }
}
