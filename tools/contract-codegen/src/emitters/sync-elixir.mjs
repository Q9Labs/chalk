import { execFile } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";
import { codegenPath, loadSyncContract, syncProtocolVersion } from "./sync-contract.mjs";

const execute = promisify(execFile);
const sourceDirectory = path.dirname(fileURLToPath(import.meta.url));
const syncDirectory = path.resolve(sourceDirectory, "../../../../apps/sync");
const protocolVersion = syncProtocolVersion();
const outputPath = codegenPath("CODEGEN_SYNC_ELIXIR_OUTPUT_PATH", protocolVersion === 1 ? "apps/sync/lib/chalk_sync/contract/generated.ex" : `apps/sync/lib/chalk_sync/contract/generated_v${protocolVersion}.ex`);
const contract = await loadSyncContract(undefined, protocolVersion);
const source = contract.version === 1 ? renderElixir(contract) : renderV3Elixir(contract);

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, source);
await execute("mix", ["format", outputPath], { cwd: syncDirectory });

/**
 * @param {Record<string, any>} contract
 */
function renderElixir(contract) {
  const snapshot = contract.welcome.modes.find((mode) => mode.id === "snapshot").snapshot;
  const commands = contract.commands.slice().sort((left, right) => left.name.localeCompare(right.name));
  const metadata = {
    version: contract.version,
    protocol: contract.protocol,
    directions: contract.directions,
    correlation: contract.correlation,
    phases: contract.phases,
    continuity: contract.continuity,
    idempotency: contract.idempotency,
    closeCodes: contract.closeCodes,
    errorConnection: contract.error.connection,
  };
  const commandTypes = commands.map((command) => `:${command.name}`).join(" | ");
  const eventClauses = contract.events.map(renderEventClause).join("\n\n");
  const ackClauses = contract.acks.map(renderAckClause).join("\n");
  const correlationType = renderCorrelationType(contract.correlation.optionalTopLevelFields);
  const correlationFieldKeys = renderCorrelationFieldKeys(contract.correlation.optionalTopLevelFields);
  const correlationValidation = renderCorrelationValidation(contract.correlation.optionalTopLevelFields);

  return `# Generated by tools/contract-codegen/src/emitters/sync-elixir.mjs
# Do not edit by hand.
defmodule ChalkSync.Contract.Generated do
  @moduledoc false

  @protocol_version ${contract.protocol.value}
  @client_commands %{${commands.map((command) => `${inspectString(command.name)} => :${command.name}`).join(", ")}}
  @correlation_field_keys ${correlationFieldKeys}
  @close_codes ${renderCloseCodes(contract.closeCodes)}
  @metadata ${renderElixirValue(metadata)}

  @type correlation_fields :: ${correlationType}
  @type command_name :: ${commandTypes}
  @type client_frame ::
          {:hello, %{required(:token) => String.t(), required(:cursor) => non_neg_integer() | nil, ${renderCorrelationTypeEntries(contract.correlation.optionalTopLevelFields)}}}
          | {:command, %{required(:command_id) => String.t(), required(:name) => command_name(), required(:payload) => map(), ${renderCorrelationTypeEntries(contract.correlation.optionalTopLevelFields)}}}
          | {:ping, correlation_fields()}

  def protocol_version, do: @protocol_version
  def client_commands, do: @client_commands
  def close_code(code), do: Map.get(@close_codes, code)
  def metadata, do: @metadata

  @spec decode_client_frame(map()) :: {:ok, client_frame()} | {:error, atom()}
  def decode_client_frame(frame) when is_map(frame) do
    if valid_correlation_fields?(frame) do
      case Map.fetch(frame, "type") do
        :error -> {:error, :missing_type}
        {:ok, "hello"} -> decode_hello(frame)
        {:ok, "command"} -> decode_command(frame)
        {:ok, "ping"} -> {:ok, {:ping, correlation_fields(frame)}}
        {:ok, _type} -> {:error, :unknown_type}
      end
    else
      {:error, :invalid_correlation_fields}
    end
  end

  def decode_client_frame(_frame), do: {:error, :missing_type}

  @spec valid_server_frame?(map()) :: boolean()
  def valid_server_frame?(frame) when is_map(frame), do: valid_correlation_fields?(frame) and valid_server_frame_body?(frame)
  def valid_server_frame?(_frame), do: false

  defp valid_server_frame_body?(%{"type" => "welcome", "protocol" => @protocol_version, "participant_id" => participant_id, "mode" => "snapshot", "snapshot" => snapshot}) do
    is_binary(participant_id) and valid_snapshot?(snapshot)
  end

  defp valid_server_frame_body?(%{"type" => "welcome", "protocol" => @protocol_version, "participant_id" => participant_id, "mode" => "replay", "events" => events, "control_revision" => revision}) do
    is_binary(participant_id) and valid_non_negative_integer?(revision) and is_list(events) and Enum.all?(events, &valid_event_frame?/1)
  end

  defp valid_server_frame_body?(%{"type" => "event"} = frame), do: valid_event_frame?(frame)

${ackClauses}

  defp valid_server_frame_body?(%{"type" => "error", "code" => code, "message" => message}), do: is_binary(code) and is_binary(message)
  defp valid_server_frame_body?(%{"type" => "pong"}), do: true
  defp valid_server_frame_body?(_frame), do: false

  defp decode_hello(frame) do
    case Map.fetch(frame, "protocol") do
      {:ok, @protocol_version} ->
        case Map.get(frame, "token") do
          token when is_binary(token) -> decode_hello_cursor(frame, token)
          _ -> {:error, :invalid_hello}
        end

      {:ok, _version} ->
        {:error, :unsupported_protocol}

      :error ->
        {:error, :invalid_hello}
    end
  end

  defp decode_hello_cursor(frame, token) do
    case control_cursor(frame) do
      :missing -> {:ok, {:hello, Map.merge(%{token: token, cursor: nil}, correlation_fields(frame))}}
      :invalid -> {:error, :invalid_cursor}
      {:present, nil} -> {:ok, {:hello, Map.merge(%{token: token, cursor: nil}, correlation_fields(frame))}}
      {:present, cursor} when is_integer(cursor) and cursor >= 0 -> {:ok, {:hello, Map.merge(%{token: token, cursor: cursor}, correlation_fields(frame))}}
      {:present, _cursor} -> {:error, :invalid_cursor}
    end
  end

  defp decode_command(frame) do
    case {Map.get(frame, "command_id"), Map.get(frame, "name")} do
      {command_id, name} when is_binary(command_id) and is_binary(name) ->
        case @client_commands do
          %{^name => command} -> decode_command_payload(frame, command_id, command)
          _ -> {:error, :unknown_command}
        end

      _ ->
        {:error, :invalid_command}
    end
  end

  defp decode_command_payload(frame, command_id, command) do
    case Map.get(frame, "payload", %{}) do
      payload when is_map(payload) -> {:ok, {:command, Map.merge(%{command_id: command_id, name: command, payload: payload}, correlation_fields(frame))}}
      _ -> {:error, :invalid_payload}
    end
  end

  defp control_cursor(frame) do
    with {:ok, streams} <- optional_map(frame, "streams"),
         {:ok, control} <- optional_map(streams, "control") do
      case Map.fetch(control, "cursor") do
        {:ok, cursor} -> {:present, cursor}
        :error -> :missing
      end
    else
      :missing -> :missing
      :invalid -> :invalid
    end
  end

  defp optional_map(container, key) do
    case Map.fetch(container, key) do
      {:ok, value} when is_map(value) -> {:ok, value}
      {:ok, _value} -> :invalid
      :error -> :missing
    end
  end

  defp valid_snapshot?(%{"control_revision" => revision, "participants" => participants}) do
    valid_non_negative_integer?(revision) and is_list(participants) and Enum.all?(participants, &valid_participant?/1)
  end

  defp valid_snapshot?(_snapshot), do: false

  defp valid_participant?(%{"participant_id" => participant_id, "display_name" => display_name, "hand_raised" => hand_raised}) do
    is_binary(participant_id) and is_binary(display_name) and is_boolean(hand_raised)
  end

  defp valid_participant?(_participant), do: false

${eventClauses}

  defp valid_event_frame?(_frame), do: false

  defp valid_revision_pair?(base_revision, revision) do
    valid_non_negative_integer?(base_revision) and valid_positive_integer?(revision) and revision == base_revision + 1
  end

  defp valid_non_negative_integer?(value), do: is_integer(value) and value >= 0
  defp valid_positive_integer?(value), do: is_integer(value) and value >= 1

  defp correlation_fields(frame) do
    Enum.reduce(@correlation_field_keys, %{}, fn {wire_key, field}, fields ->
      case Map.fetch(frame, wire_key) do
        {:ok, value} -> Map.put(fields, field, value)
        :error -> fields
      end
    end)
  end

  defp valid_correlation_fields?(frame), do: ${correlationValidation}
end
`;
}

